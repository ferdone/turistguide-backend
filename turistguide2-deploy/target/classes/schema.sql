CREATE TABLE IF NOT EXISTS SEVAERDIGHED (
    id INT AUTO_INCREMENT PRIMARY KEY,
    navn VARCHAR(255) NOT NULL,
    lokation VARCHAR(255) NOT NULL,
    beskrivelse TEXT NOT NULL,
    bedoemmelse DECIMAL(3,2) DEFAULT 0.00
);

CREATE TABLE IF NOT EXISTS BRUGER (
    id INT AUTO_INCREMENT PRIMARY KEY,
    brugernavn VARCHAR(100) UNIQUE NOT NULL,
    email VARCHAR(255) UNIQUE NOT NULL,
    adgangskode VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS ANMELDELSE (
    id INT AUTO_INCREMENT PRIMARY KEY,
    bruger_id INT,
    sevaerdighed_id INT,
    bedoemmelse INT,
    kommentar TEXT,
    oprettet_dato TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (bruger_id) REFERENCES BRUGER(id) ON DELETE CASCADE,
    FOREIGN KEY (sevaerdighed_id) REFERENCES SEVAERDIGHED(id) ON DELETE CASCADE
);